#!/usr/bin/env bash
# Run a build script on a Mac — locally if we ARE a Mac, otherwise via the
# buildhost broker. Used by Taskfile to keep individual tasks one-liners.
#
# Env passthrough (Linux → Mac): a few well-known env vars get baked into a
# generated wrapper that the broker runs as the entry script. The wrapper
# sets them, then exec's the requested build script. No broker protocol
# change needed.
#
# Usage: ./scripts/run-on-mac.sh <build-script> [extra args...]
set -euo pipefail

SCRIPT="${1:-}"
if [ -z "$SCRIPT" ]; then
  echo "usage: $0 <build-script> [args...]" >&2
  exit 2
fi

if [ "$(uname -s)" = "Darwin" ]; then
  exec "$@"
fi

cd "$(dirname "$0")/.."

SERVER="${BUILDHOST:-http://localhost:3003}"
TARBALL="$(mktemp -t source.XXXXXX.tar.gz)"
OUTPUT="$(mktemp -t output.XXXXXX.log)"

# Generate a wrapper that bakes env vars in and exec's the real script.
# Stable name so cleanup is easy; .gitignored.
WRAPPER="scripts/_envwrap.sh"
SCRIPT_REL="${SCRIPT#./}"

trap 'rm -f "$TARBALL" "$OUTPUT" "$WRAPPER"' EXIT

cat > "$WRAPPER" <<EOF
#!/usr/bin/env bash
# Auto-generated by scripts/run-on-mac.sh. Carries env from the Linux
# dispatcher into the Mac runner. Edit run-on-mac.sh, not this file.
export BACKEND_ENDPOINT='${BACKEND_ENDPOINT:-localhost:7777}'
exec './${SCRIPT_REL}' "\$@"
EOF
chmod +x "$WRAPPER"

echo "Tarballing source (mobile + scripts)..."
tar czf "$TARBALL" go.mod go.sum mobile scripts

echo "Submitting to $SERVER (entry=$WRAPPER → $SCRIPT_REL, streaming output)..."
echo "----------------------------------------"
curl -sS -N -X POST --data-binary "@$TARBALL" \
  "$SERVER/build?script=$WRAPPER" | tee "$OUTPUT"
echo "----------------------------------------"

if tail -10 "$OUTPUT" | grep -q "^__BUILD_STATUS:ok__$"; then
  echo "SUCCEEDED"
elif tail -10 "$OUTPUT" | grep -q "^__BUILD_STATUS:fail__"; then
  echo "FAILED"
  exit 1
else
  echo "UNKNOWN status — no marker found (connection dropped?)"
  exit 2
fi
